import mongoose from 'mongoose';

const MarketingPlanSchema = new mongoose.Schema(
  {
    type: {
      type: String,
      enum: ['physical', 'digital'],
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    area: {
      type: String,
      required: true,
    },
    expectedROAS: {
      type: Number,
      required: true,
    },
    startDate: {
      type: Date,
      required: true,
    },
    endDate: {
      type: Date,
      required: true,
    },
    managedBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
  },
  { timestamps: true },
);

export default mongoose.model('MarketingPlan', MarketingPlanSchema);
